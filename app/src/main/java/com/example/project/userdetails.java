package com.example.project;

public class userdetails {
    private String Name;
    private String Gender;
    private String Dob;
    private int Age;
    private int Phone;
    private String Hometown;
    private String Studiedat;
    private String Profession;
    private String Interestedto;
    private String Favourites;

    public userdetails() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        this.Gender = gender;
    }

    public String getDob() {
        return Dob;
    }

    public void setDob(String dob) {
        this.Dob = dob;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int age) {
        this.Age = age;
    }

    public int getPhone() {
        return Phone;
    }

    public void setPhone(int phone) {
        this.Phone = phone;
    }

    public String getHometown() {
        return Hometown;
    }

    public void setHometown(String hometown) {
        this.Hometown = hometown;
    }

    public String getStudiedat() {
        return Studiedat;
    }

    public void setStudiedat(String studiedat) {
        this.Studiedat = studiedat;
    }

    public String getProfession() {
        return Profession;
    }

    public void setProfession(String profession) {
        this.Profession = profession;
    }

    public String getInterestedTo() {
        return Interestedto;
    }

    public void setInterestedTo(String interestedTo) {
        this.Interestedto = interestedTo;
    }

    public String getFavourites() {
        return Favourites;
    }

    public void setFavourites(String favourites) {
        this.Favourites = favourites;
    }
}
